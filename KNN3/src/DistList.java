
public class DistList {
	private String label;
	private double [] dist;
	
	
	
	public DistList(String label, double[] dist) {
		super();
		this.label = label;
		this.dist = dist;
	}



	public String getLabel() {
		return label;
	}



	public void setLabel(String label) {
		this.label = label;
	}



	public double[] getDist() {
		return dist;
	}



	public void setDist(double[] dist) {
		this.dist = dist;
	}
	
	
	

}
